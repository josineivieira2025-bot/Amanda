export function resolveServiceKey(slug) {
  return aliasMap[slug] || slug;
}

export function getServiceContent(slug) {
  const key = resolveServiceKey(slug);
  return serviceContent[key];
}

export function getServiceRoute(slug) {
  const content = getServiceContent(slug);
  return content?.seoRoute || content?.legacyRoutes?.[0] || '/';
}
